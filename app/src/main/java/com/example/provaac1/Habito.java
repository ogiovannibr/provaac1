package com.example.provaac1;

public class Habito {
    public int id;
    public String nome;
    public String descricao;
    public boolean feito;

    public Habito(int id, String nome, String descricao, boolean feito) {
        this.id = id;
        this.nome = nome;
        this.descricao = descricao;
        this.feito = feito;
    }
}
